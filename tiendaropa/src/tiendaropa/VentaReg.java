    //al pulsar el boton de registro se registrara la venta
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        //variable auxiliar para controlar los errores
        int aux=0;
        
        //se obtiene los valores de los campos
        String nombre = jTNomPro.getText();
        String empleado = (String) jCEmpleado.getSelectedItem();
        String cantidad = jTCantidad.getText();
        
        //se comprueba que el campo cantidad contenga un valor numerico
        Pattern pat = Pattern.compile("\\D");
        Matcher mat = pat.matcher(cantidad);
       
        //si el nombre es igual a Producto muenstra un mensaje
        if(nombre.equals("Producto")){
            JOptionPane.showMessageDialog(null, "Debe introducir el nombre del producto");
            aux=1;
        }
        
        //si el campo es igual a 0 o no contiene valores numericos muestra un error
        if(cantidad.equals("0")){
            JOptionPane.showMessageDialog(null, "Debe introducir un valor superior a 0");
            aux=1;
        }else if(mat.matches()){
            JOptionPane.showMessageDialog(null, "Debe introducir un n√∫mero en el campo cantidad");
            aux=1;
        }

        //si el desplegabel esta seleccionado el index 0 muestra un mensaje
        if(jCEmpleado.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un empleado");
            aux=1;
        }
        
        //si hay errores muestra un mensaje, si no inserta la venta en la tabla
        if(aux==1){
            JOptionPane.showMessageDialog(null, "Compruebe que ha rellenado los campos correctamente");
        }else{
            try{
                Connection con = this.Conexion();
                
                PreparedStatement sentencia;
                sentencia = con.prepareStatement("INSERT INTO productos VALUES (?,?,?,?) ");
                
                int can = Integer.parseInt(cantidad);
                int emp = Integer.parseInt(empleado);
                
                sentencia.setInt(1,0);
                sentencia.setString(2,nombre);
                sentencia.setInt(3, emp);
                sentencia.setInt(4, can);
                
                sentencia.executeUpdate();
              
                JOptionPane.showMessageDialog(null, "Se ha registrado la venta.");
                
            }catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

   
